export function findDivisions(corpName) {
  if (!corpName) return null
  const n = corpName.replace(/\(주\)|주식회사|\s/g, '')
  for (const key of Object.keys(COMPANY_DIVISIONS)) {
    if (n.includes(key.replace(/\s/g, ''))) return { key, divisions: COMPANY_DIVISIONS[key] }
  }
  return null
}
